using ComLib.clsMgr;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ComLib
{
    public class LogDataList
    {
        private Alarms _action;
        private string _contents;
        private string _pageName;

        public string PageName
        {
            get { return _pageName; }
            set { _pageName = value; }
        }


        public LogDataList(Alarms __action, string __pageName, string __contents)
        {
            _action = __action;
            _pageName = __pageName;
            _contents = __contents;
        }

        public Alarms Action
        {
            get { return _action; }
            set { _action = value; }
        }
        public string Contents
        {
            get { return _contents; }
            set { _contents = value; }
        }

    }
}
